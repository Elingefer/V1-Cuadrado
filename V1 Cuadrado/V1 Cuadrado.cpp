#include <iostream>

int main() {
    // Definir la longitud del lado del cuadrado en centímetros
    double lado = 5.0;

    // Calcular el área del cuadrado (lado * lado)
    double area = lado * lado;

    // Mostrar el resultado
    std::cout << "El área del cuadrado con un lado de " << lado << " cm es: " << area << " cm^2" << std::endl;

    return 0;
}

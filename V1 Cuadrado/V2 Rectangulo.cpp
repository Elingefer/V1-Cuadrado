#include <iostream>

int main() {
    // Definir la longitud de los lados del rectángulo en centímetros
    double largo = 23.0;
    double ancho = 23.0;

    // Calcular el área del rectángulo (largo * ancho)
    double area = largo * ancho;

    // Mostrar el resultado
    std::cout << "El área del rectángulo con largo de " << largo << " cm y ancho de " << ancho << " cm es: " << area << " cm^2" << std::endl;

    return 0;
}
